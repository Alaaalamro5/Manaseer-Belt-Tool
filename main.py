# مساعد المشغل الميداني - نسخة مجمع المناصير المحدثة
print("🚀 نظام فحص السيور والإنتاجية")
print("------------------------------")

try:
    # 1. إدخال البيانات من الميدان
    belt_id = input("رقم السير (مثلاً BC4): ")
    v_design = float(input("السرعة التصميمية (m/s): "))
    v_actual = float(input("السرعة المقاسة بالتاكوميتر (m/s): "))

    # 2. الحسابات التقنية
    # حساب نسبة الانزلاق (Slip)
    slip = ((v_design - v_actual) / v_design) * 100
    
    # حساب الإنتاجية اللحظية (بافتراض حمولة 50 كغم لكل متر)
    tons_per_hour = (v_actual * 50 * 3600) / 1000

    # 3. عرض التقرير الفني
    print(f"\n📊 تقرير فحص: {belt_id}")
    print(f"🔹 نسبة الانزلاق: {slip:.2f}%")
    print(f"🔹 الإنتاجية اللحظية: {tons_per_hour:.1f} Ton/hr")

    # 4. التوصية للمشغل الأول
    if slip > 5:
        print("⚠️ التوصية: انزلاق مرتفع! افحص الشدادات (Tensioners).")
    elif slip < 0:
        print("❓ تنبيه: السرعة المقاسة أعلى من التصميمية! تأكد من القراءة.")
    else:
        print("✅ الحالة: السير يعمل بكفاءة ممتازة.")

except ValueError:
    print("❌ خطأ: يرجى إدخال أرقام فقط واستخدام النقطة (.) للفواصل.")

print("\n[انتهى التقرير - عمل موفق يا بطل]")
